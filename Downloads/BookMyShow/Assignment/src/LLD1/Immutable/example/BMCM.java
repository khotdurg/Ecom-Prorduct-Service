package LLD1.Immutable.example;

public final class BMCM {
    private final int id;
    private final String name;

    public BMCM(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
